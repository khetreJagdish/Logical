public class String3 {
		public static void main(String[] args) {
			
			int i = 23454;
			System.out.printf("%d",i);
			System.out.printf("\n(%d)", i);
			System.out.printf("\n(%10d)",i);
			System.out.printf("\n(%3d)", i);
			System.out.printf("\n(%1$12d)",i);
		}
}
